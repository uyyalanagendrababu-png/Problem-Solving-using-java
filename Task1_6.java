public class Task1_6 {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4};

        System.out.println("All possible pairs:");
        for (int i = 0; i < arr.length; i++) {
            for (int j = i + 1; j < arr.length; j++) {
                System.out.println("(" + arr[i] + ", " + arr[j] + ")");
            }
        }
    }
}